import Head from 'next/head';
import { css, useTheme } from '@emotion/react';
import { useEffect } from 'react';
import { useDispatch, useSelector } from 'react-redux';
import { Card, Layout } from '../containers';
import { Typography } from '../components';
import { GET_PRODUCTS_ACTION } from '../actions';

const Home = ({ products }) => {
  const theme = useTheme();
  const dispatch = useDispatch();

  return (
    <Layout>
      <Head>
        <title>MBK: Shop</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <div className="shop">
        <div className="Shop__tittle">
          <Typography variant="h2">Products</Typography>
          <Typography variant="body1">
            Order it for you or for your beloved ones
            {' '}
          </Typography>
        </div>

        <div className="Shop__products">
          {
            products && products.map((item) => (
              <Card key={item.id} title={item.name} imageAlt={item.name} imageSrc={item.img} path={`/Products/${item.id}`} price={item.price} />
            ))
          }
        </div>
      </div>
      <style jsx>
        {`
          .shop {
            display: flex;
            flex-direction: column;
            align-items: center;
          }
          .Shop__tittle {
            text-align: center;
          }
          .Shop__products {
            margin-top: 30px;
            display: flex;
            justify-content: center;
            align-items: center;
            flex-wrap: wrap;
          }
        `}
      </style>
    </Layout>
  );
};

Home.getInitialProps = async ({ reduxStore }) => {
  await reduxStore.dispatch(GET_PRODUCTS_ACTION());
  const { products } = reduxStore.getState();
  return {
    products: products.products,
  };
};

export default Home;
